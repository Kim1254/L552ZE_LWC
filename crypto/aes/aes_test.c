void aes_test() {
	printf("Key:\n");
	print_hex(key, 16);
	
	printf("Plain Text:\n");
	print_hex(source_plain, 64);
	
	printf("Cipher Text:\n");
	
	uint8_t plain[64], cipher[64];
	memcpy(plain, source_plain, STRING_LENGTH);
	
	uint8_t i;
	memcpy(cipher, plain, STRING_LENGTH);
	
	// Initialize
	struct AES_ctx ctx;
	AES_init_ctx(&ctx, key);
	
	int num_enc = STRING_LENGTH / 16;
	
	// do enc
	for (i = 0; i < num_enc; ++i)
		AES_ECB_encrypt(&ctx, cipher + (i * 16));
	
	// do dec
	memcpy(plain, cipher, STRING_LENGTH);
	
	for (i = 0; i < num_enc; ++i)
		AES_ECB_decrypt(&ctx, plain + (i * 16));
	
	print_hex(cipher, 64);
	
	printf("Plain Text:\n");
	print_hex(plain, 64);
}
